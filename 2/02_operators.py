a = 5
b = 4

# Arithmetic operators
print("The value of 5+4 is ", a + b)
print("The value of 5-4 is ", a - b)
print("The value of 5/4 is ", a / b)
print("The value of 5*4 is ", a * b)

# Assignment operators

c = 10
c += 5

d = 10
d -= 5

e = 10
e *= 5

f = 10
f /= 5
print(c)
print(d)
print(e)
print(f)

# Comparison operators

b = (14 <= 7)
c = (14 >= 7)
d = (12 != 12)
e = (10 == 10)
print(b)
print(c)
print(d)
print(e)

# Logical operator

bool1 = True
bool2 = False

# If both variables are true it will return True and in every other case it returns False
print("The value of bool1 and bool2 is", bool1 and bool2,)
# if one of the variables is true it will return true and in every other case it returns False
print("The value of bool1 or bool2 is", bool1 or bool2)
# it will give the opposite of the boolean (true becomes falsw and false becomes true)
print("The value of not bool2 is", not bool2)
